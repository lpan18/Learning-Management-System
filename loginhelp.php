<div align="center"><font size='5'><b>登录帮助</b></font></div><br><br>

<font size='4'>1、请首先选择您的用户类型，普通学生or学生管理员or教师，单选框默认您为普通学生。注意，学生管理员只能从管理账户登录。<br><br>
2、请输入您的用户名，普通学生和管理员输入学号，老师输入教工号。学生帐号为7位数字，老师帐号为5位数字。<br><br>
3、请输入您的密码，若您是学生或管理员，第一次登录时初始密码为学号，若您是老师，第一次登录初始密码为教工号，登录后请及时修改您的密码，并妥善保存。<br><br>
4、完成以上步骤即可点击登录按钮，进入作业发布与提交系统。完成作业发布或提交等工作。<br><br>
5、若你的密码丢失，可以联系课程的管理员或者老师为您重置密码。<br><br>

<br><br>谢谢关注，祝您愉快！<br><br>
